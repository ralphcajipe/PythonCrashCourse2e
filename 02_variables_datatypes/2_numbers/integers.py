print ("add (+), subtract (-), multiply (*), and divide (/)")
print(f"2 + 3 = ", 2 + 3)
print(f"3 - 2 = ", 3 - 2)
print(f"2 * 3 = ", 2 * 3)
print(f"3 / 2 = ", 3 / 2, "\n")

print("two multiplication symbols to represent exponents")
print(f"3 ** 2 = ", 3 ** 2)
print(f"3 ** 3 = ", 3 ** 3)
print(f"10 ** 6 = ", 10 ** 6, "\n")

print("order of operations PEMDAS")
print(f"2 + 3*4 = ", 2 + 3*4)
print(f"(2 + 3) * 4 = ", (2 + 3) * 4)